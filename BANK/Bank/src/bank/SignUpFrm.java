    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String name = jTextField1.getText();
        String email = jTextField2.getText();
        String month = (String)jComboBox1.getSelectedItem();
        String day = (String)jComboBox2.getSelectedItem();
        String year = (String)jComboBox3.getSelectedItem();
        String dob = month+"/"+day+"/"+year;
        String address = jTextArea1.getText();
        String uname = jTextField3.getText();
        String pass = jPasswordField1.getText();
        String confirmpass = jPasswordField2.getText();
        
        if(name.isEmpty())
            JOptionPane.showMessageDialog(null, "Name Required");
        else if(email.isEmpty())
            JOptionPane.showMessageDialog(null, "Email Required");
        else if(address.isEmpty())
            JOptionPane.showMessageDialog(null, "Address Required");
        else if(uname.isEmpty())
            JOptionPane.showMessageDialog(null, "Username Required");
        else if(pass.isEmpty())
            JOptionPane.showMessageDialog(null, "Password Required");
        else if(confirmpass.isEmpty())
            JOptionPane.showMessageDialog(null, "Please confirm password");
        else if(!(pass.equals(confirmpass)))
        {
            JOptionPane.showMessageDialog(null, "Password does not match!!!!");
            jPasswordField1.setText(null);
            jPasswordField2.setText(null);
        }
        else if(new SignUpMaker().signup(name, dob, email, address, uname, pass))
        {
            JOptionPane.showMessageDialog(null, "Signed Up successfully!!");
            this.dispose();
            MainFrm mf = new MainFrm();
            mf.setVisible(true);
            Dimension d = Toolkit.getDefaultToolkit().getScreenSize();
            mf.setSize(d);
        }
        else
            JOptionPane.showMessageDialog(null, "Error while inserting in database");
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

