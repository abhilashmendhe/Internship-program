    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String name = jTextField1.getText();
        String month = (String) jComboBox1.getSelectedItem();
        String day = (String) jComboBox2.getSelectedItem();
        String year = (String) jComboBox3.getSelectedItem();
        String dob = month +"/"+ day +"/"+ year;
        String email = jTextField2.getText();
        String address = jTextArea1.getText();
        String uid = jTextField3.getText();
        String password = jPasswordField1.getText();
        String confpassword = jPasswordField2.getText();
        
        if(name.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter name");
        else if(email.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter email ID");
        else if(address.isEmpty())
            JOptionPane.showMessageDialog(null, "Enter Address");
        else if(uid.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter uid");
        else if(password.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter password");
        else if(!confpassword.equals(password))
        {
            JOptionPane.showMessageDialog(null, "Password does not match");
            jPasswordField1.setText(null);
            jPasswordField2.setText(null);
        }
        else 
        {
            if(new CheckUserPresentMaker().checkUser(uid))
            {
                JOptionPane.showMessageDialog(null, "UID exists please use different uid");
                jTextField3.setText(null);
            }
            else if(new SignUpMaker().signUp(name,dob,email,address,uid,password))
            {
                JOptionPane.showMessageDialog(null, "Signed Up Sucessfully!!!");
                jTextArea1.setText(null);
                jTextField1.setText(null);
                jTextField2.setText(null);
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                jComboBox3.setSelectedIndex(0);
                jPasswordField1.setText(null);
                jPasswordField2.setText(null);
            }
            else
                JOptionPane.showMessageDialog(null, "Error while Signing Up!!!!");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

