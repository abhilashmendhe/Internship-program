    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String month = (String) jComboBox1.getSelectedItem();
        String day = (String) jComboBox2.getSelectedItem();
        String year = (String) jComboBox3.getSelectedItem();
        String dob = month +"/"+ day +"/"+ year;
        
        String email = jTextField2.getText();
        String mobile = jTextField3.getText();
        
        
        
        String pass = jPasswordField1.getText();
        String confpass = jPasswordField2.getText();
        
        if(name.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter name");
        else if(email.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter email");
        else if(mobile.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter mobile number");
        else if(!(mobile.length()==10))
            JOptionPane.showMessageDialog(null, "Number should be of 10 digits");
         else if(pass.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter password");
        else if(confpass.isEmpty())
            JOptionPane.showMessageDialog(null, "Please confirm password");
        else if(!confpass.equals(pass))
            JOptionPane.showMessageDialog(null, "Password does not match");
        else if(new SignUpUpdateMaker().signupupdate(name, dob, email, mobile, MainFrame.username, pass))
        {
            JOptionPane.showMessageDialog(null, "Profile Updated Sucessfully!!!");
            this.dispose();
            MainFrame mf = new MainFrame();
        mf.setVisible(true);
        mf.setSize(1920, 1080);
        }
        else
            JOptionPane.showMessageDialog(null, "Error while Updatating profile up!!");
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

