    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        
        String path = jTextField1.getText();
        
        String content;
        
        try {
            
            StringBuffer sb = new StringBuffer();
            FileInputStream fis = new FileInputStream(path);
            
            int x = 0;
            char ch = 0;
            
            do{
                x = fis.read();
                if(x!=-1)
                {
                    ch = (char)x;
                    sb.append(ch);
                }
            }while(x!=-1);
            
            content = sb.toString();
            
            String words[] = content.split(" ");
            int count_words = words.length;
            String w = Integer.toString(count_words);
            jTextField2.setText(w);
            
            String lines[] = content.split("\n");
            int count_lines = lines.length;
            String l1 = Integer.toString(count_lines);
            jTextField3.setText(l1);
            
            String chars = Integer.toString(content.length());
            jTextField4.setText(chars);
        } catch (Exception e) {
        }
                // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

