    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String st1 = jTextField6.getText();
        String st2 = jTextField7.getText();
        if(st1.length()==0)
            JOptionPane.showMessageDialog(null, "Please enter acc. no. to Withdraw amt.");
        else if(st2.length()==0)
            JOptionPane.showMessageDialog(null, "Please enter amt. to Withdraw");
        else
        {
        int acc_no = Integer.parseInt(st1);
        double deposit_amt = Double.parseDouble(st2);
        for(int i=0; i<arr.length; i++)
        {
            if(acc_no==arr[i].acc_no)
            {
                if(arr[i].bal>=deposit_amt)
                {
                    arr[i].bal-=deposit_amt;
                    jTextField6.setText(null);
                    jTextField7.setText(null);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Not enough amt. to withdraw");
                    jTextField7.setText(null);
                }
            }
        }
        
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

