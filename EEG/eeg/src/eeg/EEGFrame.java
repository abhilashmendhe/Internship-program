    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            
            String train_path = Datakeeper.trainpath;
            
            String gender = (String) jComboBox1.getSelectedItem();
            String agee = (String) jComboBox2.getSelectedItem();
            String smokere = (String) jComboBox3.getSelectedItem();
            String mt = (String) jComboBox4.getSelectedItem();
            String ms = (String) jComboBox5.getSelectedItem();
            int c1 = 0;
            int c2 = 0;
            int c3 = 0;
            if(smokere.equals("YES"))
                c1 = 1;
            if(mt.equals("YES"))
                c2 = 1;
            if(ms.equals("YES"))
                c3 = 0;
            Datakeeper.gen = gender;
            Datakeeper.age = agee;
            Datakeeper.smoker = Integer.toString(c1);
            Datakeeper.med_treat = Integer.toString(c2);
            Datakeeper.med_supple = Integer.toString(c3);
            new ProcessInitiator().initProcess(train_path);
            // TODO add your handling code here:
        } catch (IOException ex) {
            Logger.getLogger(EEGFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BiffException ex) {
            Logger.getLogger(EEGFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

