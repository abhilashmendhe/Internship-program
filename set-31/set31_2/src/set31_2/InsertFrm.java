    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText(null);
        String s1 = jTextField3.getText();
        String s2 = jTextField4.getText();
        if(s1.length()==0 && s2.length()==0)
            JOptionPane.showConfirmDialog(null, "Please first add elements in array");
        else
        {
        
        
        String ele = jTextField3.getText();
        String pos = jTextField4.getText();
        
        int element = Integer.parseInt(ele);
        int position = Integer.parseInt(pos);
        
        for(int i=a.length-1; i>=position; i--)
        {
            a[i] = a[i-1];
        }
        a[position-1] = element;
        String str = "";
        for(int i=0; i<a.length; i++)
        {
            String k = Integer.toString(a[i]);
            str += "\n"+k;
        }
        jTextArea1.setText(str);
        }
        jTextField3.setText(null);
        jTextField4.setText(null);
    }//GEN-LAST:event_jButton3ActionPerformed

