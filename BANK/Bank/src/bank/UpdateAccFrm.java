    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String accno = jTextField1.getText();
        String custname = jTextField2.getText();
        String month = (String)jComboBox1.getSelectedItem();
        String day = (String)jComboBox2.getSelectedItem();
        String year = (String)jComboBox3.getSelectedItem();
        String dob = month+"/"+day+"/"+year;
        String address = jTextArea1.getText();
        String email = jTextField3.getText();
        String mobile = jTextField4.getText();
        
        
        if(new UpdateAccMaker().updateacc(accno, custname, dob, address, email, mobile))
            JOptionPane.showMessageDialog(null, "Acc. info updated sucessfully");
        else
            JOptionPane.showMessageDialog(null, "Error while updating Acc. info");
        
    }//GEN-LAST:event_jButton2ActionPerformed

