    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String password = jPasswordField1.getText();
        String confpass = jPasswordField2.getText();
        
        if(password.isEmpty())
        JOptionPane.showMessageDialog(null, "Please Enter Password");
        else if(confpass.isEmpty())
        JOptionPane.showMessageDialog(null, "Please Confirm Password");
        else if(!password.equals(confpass))
        JOptionPane.showMessageDialog(null, "Password Does not match");
        else if(new StaffCredsUpdateMaker().updateStaffCreds(username, password))
        {
            JOptionPane.showMessageDialog(null, "Staff Credentials Updated sucessfully!!");
            this.dispose();
            EmployeeLoginFrame ad = new EmployeeLoginFrame();
            Dimension d = Toolkit.getDefaultToolkit().getScreenSize();
            ad.setVisible(true);
            ad.setSize(d);
        }
        else
        JOptionPane.showMessageDialog(null, "Error while updating credentials");
        // TODO add your handling code her
    }//GEN-LAST:event_jButton1ActionPerformed

