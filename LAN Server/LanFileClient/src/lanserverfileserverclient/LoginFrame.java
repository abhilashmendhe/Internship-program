    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String uname = jTextField1.getText();
        String pass = jPasswordField1.getText();
        
        if(uname.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter name");
        else if(pass.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter password");
        else
        {
            ArrayList data = new LoginValidMaker().loginValid(uname, pass);
            if(data.size()==0)
                JOptionPane.showMessageDialog(null, "No data in database");
            else if(!uname.equals(data.get(0)) || !pass.equals(data.get(1)))
                JOptionPane.showMessageDialog(null, "Username and Password incorrect");
            else
            {
                JOptionPane.showMessageDialog(null, "Logged in sucessfully!!!");
                this.dispose();
                ClientOperationsFrame c = new ClientOperationsFrame();
                c.setVisible(true);
                Dimension d = Toolkit.getDefaultToolkit().getScreenSize(); 
                
                c.setSize(d);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

