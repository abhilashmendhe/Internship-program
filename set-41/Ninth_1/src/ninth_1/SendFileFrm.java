    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String ip = jTextField1.getText();
        String path = jTextField2.getText();
        
        if(ip.isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter IP Address of Reciever");
        else if(path.isEmpty())
            JOptionPane.showMessageDialog(rootPane, "Please select file to transfer");
        else
        {
            
            try {
                File f = new File(path);
                String filename = f.getName();
                String cont = new ReadFile().getContent(path);
                String mylocalip[] = Inet4Address.getLocalHost().toString().split("/");
                String sendcont = mylocalip[1]+"#"+cont+"#"+filename;
                new SendFile().transferFile(sendcont, ip);
                
              
            }
            // TODO add your handling code here:
            catch (UnknownHostException ex) {
                System.out.println("Error while fetching ip: "+ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

