    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ele = jTextField1.getText();
        
        int tmpfront = front;
        int tmprear = rear;
        
        if(ele.length()==0)
            JOptionPane.showMessageDialog(null, "Please enter field");
        else if(inc==a.length)
            JOptionPane.showMessageDialog(null, "Queue is full!!!!!");
        else
        {
            int element = Integer.parseInt(ele);
            a[front--] = element;
            inc++;
            jTextField1.setText(null);
            String str = "";
            for(int i=tmpfront; i<=tmprear; i++)
        {
            String k = Integer.toString(a[i]);
            str+="\n"+k;
        }
        jTextArea1.setText(str);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

