    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String accno = jTextField1.getText();
        String balance = jTextField3.getText();
        String withdrawtamt = jTextField2.getText();
        double bal = Double.parseDouble(balance);
        
        if(balance.isEmpty())
            JOptionPane.showMessageDialog(null, "Deposit field is empty");
        else if(Double.parseDouble(withdrawtamt)>bal)
            JOptionPane.showMessageDialog(null, "Insufficient balance");
        else if(Double.parseDouble(withdrawtamt)<=bal)
        {
            double withamt = Double.parseDouble(withdrawtamt);
            bal -= withamt;
            balance = Double.toString(bal);
            boolean f = new BankOpsMaker().bankamt(balance, accno);
            if(f)
                JOptionPane.showMessageDialog(null, "Withdraw sucessfull");
            else 
                 JOptionPane.showMessageDialog(null, "Error while withdrawing");
        }
           
    }//GEN-LAST:event_jButton1ActionPerformed

