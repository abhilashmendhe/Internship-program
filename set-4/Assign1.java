package set4;

public class Assign1 {

    public static void main(String args[]){
    	int a=10,b=20;

    	System.out.println("Before increment, value of a is: "+a);
    	System.out.println("After post-incr., value of a is: "+a++);
    	System.out.println("----------------------");
    	System.out.println("Before increment, value of b is: "+b);
    	System.out.println("After pre-incr., value of b is: "+ ++b);

    	}


}