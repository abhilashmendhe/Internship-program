    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String s = jTextField1.getText();
        int size;
        if(s.length()==0)
        JOptionPane.showMessageDialog(null, "Please enter size of array");

        else
        {
            size = Integer.parseInt(s);
            inc = a_inc;
            a_inc=1;
            jTextField1.setText(null);
            a = new int[size];
            JOptionPane.showMessageDialog(null, "Array has been initialized");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String s = jTextField2.getText();
        if(s.length()==0)
        JOptionPane.showMessageDialog(null, "Please add elements in array");
        else if(a_inc==0)
        {
            JOptionPane.showMessageDialog(null, "First initialize array");
            jTextField2.setText(null);
        }
        else if(inc==a.length)
        {
            JOptionPane.showMessageDialog(null, "Array is full");
            jTextField2.setText(null);
        }
        else
        {
            String str = "";
            a[inc++] = Integer.parseInt(s);
            jTextField2.setText(null);
            for(int i=0; i<inc; i++)
            {
                String k = Integer.toString(a[i]);
                str += "   "+k;
            }
            jTextArea2.setText(str);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if(a_inc==0)
        JOptionPane.showMessageDialog(null, "First Initialize array to sort");
        else if(inc==a.length)
        {
            for(int i=0; i<a.length; i++)
            {
                for(int j=0; j<a.length-1; j++)
                {
                    if(a[j]<a[j+1])
                    {
                        int tmp = a[j];
                        a[j] = a[j+1];
                        a[j+1] = tmp;
                    }
                }
            }
            String str = "";
            for(int i=0; i<a.length; i++)
            {
                String k = Integer.toString(a[i]);
                str += "   "+k;
            }
            jTextArea1.setText(str);
        }
        else
        JOptionPane.showMessageDialog(null, "Insert all elements in array");
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

