    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String s = jTextField1.getText();
        if(s.length()==0)
        JOptionPane.showMessageDialog(null, "Please enter array size");
        else
        {
            int size = Integer.parseInt(s);
            a = new int[size];
            JOptionPane.showMessageDialog(null, "Array Declared");
            jTextField1.setText(null);
            t = false;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       
        
        String ele = jTextField3.getText();
        if(ele.length()==0)
            JOptionPane.showMessageDialog(null, "Please Select Element to delete from array");
        else if(t)
            JOptionPane.showMessageDialog(null, "First Declare array");
        else if(inc==a.length)
        {
        int element = Integer.parseInt(ele);
        jTextArea1.setText(null);
        for(int i=0; i<a.length; i++)
        {
            if(element==a[i])
            pos=i;
        }

        for(int i=pos; i<a.length-1; i++)
        {
            a[i] = a[i+1];
        }

        String str = "";
        for(int i=0; i<a.length-1; i++)
        {
            String k = Integer.toString(a[i]);
            str += "\n"+k;
        }
        jTextArea1.setText(str);
        }
        else
            JOptionPane.showMessageDialog(null, "Please enter all elements in array");
    }//GEN-LAST:event_jButton3ActionPerformed
    
    
